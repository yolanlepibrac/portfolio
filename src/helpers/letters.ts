export const isLetter = (
  letterPosition: { x: number; y: number }[],
  position: { x: number; y: number; z?: number }
) => {
  return letterPosition.some((homePosition) => {
    return homePosition.x === -position.x && homePosition.y === -position.y;
  });
};
